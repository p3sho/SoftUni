grade = float(input())
if grade >= 2 and grade <= 2.99:
    print('Fail')
elif grade >= 3 and grade <= 3.49:
    print('Poor')
elif grade >= 3.5 and grade <= 4.49:
    print('Good')
elif grade >= 4.5 and grade <= 5.49:
    print('Very Good')
elif grade >= 5.5 and grade <= 6:
    print('Excellent')
